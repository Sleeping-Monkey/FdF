#include "fdf.h"

void	free_int_arr(int ***arr)
{
	size_t	i;

	if (!arr)
		return ;
	i = 0;
	while ((*arr)[i])
	{
		free((*arr)[i]);
		(*arr)[i++] = NULL;
	}
	free(*arr);
	*arr = NULL;
}
