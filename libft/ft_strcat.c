#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	unsigned	i;
	unsigned	j;

	i = 0;
	while (s1[i])
		i++;
	j = 0;
	while (s2[j])
		s1[i++] = s2[j++];
	s1[i] = 0;
	return (s1);
}
